# For standalone use or extension
def create_premiere_tools_server(name: str = "Premiere Tools") -> FastMCP:
    """Create a standalone MCP server with Premiere tools.

    Args:
        name: Server name.

    Returns:
        Configured FastMCP server.
    """
    mcp = FastMCP(name, log_level="ERROR")
    register_tools(mcp)
    return mcp
